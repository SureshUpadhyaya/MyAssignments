package Assignments.week1;

public class CheckNumbersIsPositive {
	public static void main(String[] args) {
		int val = -10;
		if (val>0) {
			System.out.println("The Integer value " +val +" is a POSITIVE number");
		}
		else 
		{
			System.out.println("The Integer value " +val +" is a NEGATIVE number");
 
		}
 
	}
}
